package com.Programs;

public class PrintTheFollowingPattern {

	public static void main(String[] args) {
		System.out.println("     j   A A   V        V   A A");
		System.out.println("    j   A  A    V       V  A   A");
		System.out.println("   j   A  A A    V     V  A  A  A");
		System.out.println("j  j  A     A     V   V   A      A");
		System.out.println("j j   A     A      V V    A      A ");

	}

}
